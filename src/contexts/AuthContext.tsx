import React, { createContext, useContext, useEffect, useState, useRef } from 'react';
import { User, Session } from '@supabase/supabase-js';
import { supabase } from '@/integrations/supabase/client';
import { UserRole } from '@/types/auth';
import { toast } from 'sonner';

export interface AuthUser {
  id: string;
  email: string;
  firstName: string;
  lastName: string;
  phone?: string;
  avatarUrl?: string;
  isActive: boolean;
  roles: string[]; // Dinamik rol isimleri - database'den ne gelirse
  currentRole: UserRole; // Kategorize edilmi≈ü rol
}

interface AuthContextType {
  user: AuthUser | null;
  session: Session | null;
  currentRole: UserRole | null;
  switchRole: (role: UserRole) => void;
  login: (email: string, password: string) => Promise<{ error?: string }>;
  createUser: (email: string, password: string, firstName: string, lastName: string, roles: UserRole[]) => Promise<{ error?: string }>;
  logout: () => Promise<void>;
  resetPassword: (email: string) => Promise<{ error?: string }>;
  isLoading: boolean;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export function AuthProvider({ children }: { children: React.ReactNode }) {
  // localStorage'dan cached user'ƒ± oku
  const getCachedUser = (): AuthUser | null => {
    try {
      const cached = localStorage.getItem('cachedAuthUser');
      if (cached) {
        return JSON.parse(cached);
      }
    } catch (error) {
      console.error('Error reading cached user:', error);
      localStorage.removeItem('cachedAuthUser');
    }
    return null;
  };

  const [user, setUser] = useState<AuthUser | null>(getCachedUser());
  const [session, setSession] = useState<Session | null>(null);
  const [currentRole, setCurrentRole] = useState<UserRole | null>(() => {
    const cachedUser = getCachedUser();
    return cachedUser?.currentRole || null;
  });
  const [isLoading, setIsLoading] = useState(true);
  const [justLoggedIn, setJustLoggedIn] = useState(false);
  const isCreatingUserRef = useRef(false); // Kullanƒ±cƒ± olu≈üturma flag'i - useRef ile global

  // Fetch user profile and roles
  const fetchUserProfile = async (authUser: User) => {
    try {
      // Get user profile
      const { data: profile, error: profileError } = await supabase
        .from('users')
        .select('*')
        .eq('auth_user_id', authUser.id)
        .single();

      if (profileError) {
        // PGRST116 = no rows found - silinmi≈ü kullanƒ±cƒ± veya yeni kullanƒ±cƒ± olu≈üturma sƒ±rasƒ±nda
        if (profileError.code === 'PGRST116') {
          console.log('üë§ Profile not found (deleted user or new user being created):', authUser.email);
        } else {
          console.error('Error fetching profile:', profileError);
        }
        return null;
      }

      // Get user roles from JSONB field - dinamik olarak her rol deƒüerini al
      const roles = Array.isArray(profile.roles) ? profile.roles as string[] : ['kullanici'] as string[];
      
      // Dinamik rol kategorisi belirleme - sadece role deƒüerine bak, isim/koda bakma
      const determineRoleCategory = (userRoles: string[]): UserRole => {
        // Admin kontrol√º - en y√ºksek yetki
        if (userRoles.includes('admin')) return 'admin';
        
        // Temsilci kontrol√º
        if (userRoles.includes('temsilci')) return 'temsilci';
        
        // Beyin antren√∂r√º kontrol√º
        if (userRoles.includes('beyin_antrenoru')) return 'beyin_antrenoru';
        
        // Kullanƒ±cƒ± kontrol√º - varsayƒ±lan
        if (userRoles.includes('kullanici')) return 'kullanici';
        
        // Varsayƒ±lan (eski kayƒ±tlar i√ßin)
        return 'kullanici';
      };
      
      const primaryRole = determineRoleCategory(roles);
      
      // Stored role'√º kontrol et, eƒüer user'ƒ±n rollerinde varsa onu kullan
      const storedRole = localStorage.getItem('currentRole') as UserRole;
      const currentRole = (storedRole && roles.includes(storedRole)) ? storedRole : primaryRole;
      
      // User profile and roles loaded successfully

      const authUserData: AuthUser = {
        id: profile.id,
        email: profile.email,
        firstName: profile.first_name,
        lastName: profile.last_name,
        phone: profile.phone,
        avatarUrl: profile.avatar_url,
        isActive: profile.is_active,
        roles,
        currentRole: currentRole
      };

      // User profilini localStorage'a kaydet
      try {
        localStorage.setItem('cachedAuthUser', JSON.stringify(authUserData));
      } catch (error) {
        console.error('Error caching user data:', error);
      }

      return authUserData;
    } catch (error) {
      console.error('Error in fetchUserProfile:', error);
      return null;
    }
  };

  // Initialize auth state
  useEffect(() => {
    // Set up auth state listener
    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        setSession(session);
        
        if (session?.user) {
          // Fetch user profile with delay to avoid blocking
          setTimeout(async () => {
            const userData = await fetchUserProfile(session.user);
            if (userData) {
              setUser(userData);
              setCurrentRole(userData.currentRole);
              
              // Sadece ger√ßek giri≈ü i≈üleminde success mesajƒ± ver
              if (justLoggedIn) {
                toast.success('Ba≈üarƒ±yla giri≈ü yapƒ±ldƒ±!');
                setJustLoggedIn(false); // Flag'i reset et
              }
            } else {
              // Kullanƒ±cƒ± olu≈üturma sƒ±rasƒ±nda bu kontrol√º atla
              if (!isCreatingUserRef.current) {
                // Profil bulunamadƒ± - muhtemelen silinmi≈ü kullanƒ±cƒ±
                console.log('üö´ User profile not found, signing out user:', session.user.email);
                toast.error('Kullanƒ±cƒ± hesabƒ±nƒ±z sistemden kaldƒ±rƒ±lmƒ±≈ü. L√ºtfen y√∂neticinizle ileti≈üime ge√ßin.');
                await supabase.auth.signOut();
                setUser(null);
                setCurrentRole(null);
                setSession(null);
                setJustLoggedIn(false); // Flag'i reset et
                // localStorage'dan currentRole'√º temizle
                localStorage.removeItem('currentRole');
              } else {
                console.log('üë§ Profile not found (new user being created):', session.user.email);
              }
            }
          }, 0);
        } else {
          setUser(null);
          setCurrentRole(null);
          setJustLoggedIn(false); // √áƒ±kƒ±≈ü durumunda flag'i reset et
          // localStorage'dan currentRole ve cachedAuthUser'ƒ± temizle
          localStorage.removeItem('currentRole');
          localStorage.removeItem('cachedAuthUser');
          setIsLoading(false);
        }
      }
    );

    // Check for existing session
    supabase.auth.getSession().then(({ data: { session } }) => {
      // Auth session loaded
      
      setSession(session);
      if (session?.user) {
        // Eƒüer cached user varsa ve session user ID'si e≈üle≈üiyorsa, hƒ±zlƒ± y√ºkleme yap
        const cachedUser = getCachedUser();
        if (cachedUser && session.user.email === cachedUser.email) {
          // Cached user'ƒ± kullan, arka planda g√ºncelle
          setUser(cachedUser);
          setCurrentRole(cachedUser.currentRole);
          setIsLoading(false); // Hemen y√ºklemeyi bitir - flash √∂nlenir!
          
          // Arka planda profili g√ºncelle (sessizce)
          fetchUserProfile(session.user).then(userData => {
            if (userData) {
              setUser(userData);
              setCurrentRole(userData.currentRole);
            } else {
              // Profil bulunamadƒ± - muhtemelen silinmi≈ü kullanƒ±cƒ±
              console.log('üö´ User profile not found, signing out user:', session.user.email);
              toast.error('Kullanƒ±cƒ± hesabƒ±nƒ±z sistemden kaldƒ±rƒ±lmƒ±≈ü. L√ºtfen y√∂neticinizle ileti≈üime ge√ßin.');
              supabase.auth.signOut();
              setUser(null);
              setCurrentRole(null);
              setSession(null);
              localStorage.removeItem('currentRole');
              localStorage.removeItem('cachedAuthUser');
            }
          });
        } else {
          // Cache yok veya e≈üle≈ümiyor, normal y√ºkleme yap
          setTimeout(async () => {
            const userData = await fetchUserProfile(session.user);
            if (userData) {
              setUser(userData);
              setCurrentRole(userData.currentRole);
            } else {
              // Profil bulunamadƒ± - muhtemelen silinmi≈ü kullanƒ±cƒ±
              console.log('üö´ User profile not found, signing out user:', session.user.email);
              toast.error('Kullanƒ±cƒ± hesabƒ±nƒ±z sistemden kaldƒ±rƒ±lmƒ±≈ü. L√ºtfen y√∂neticinizle ileti≈üime ge√ßin.');
              await supabase.auth.signOut();
              setUser(null);
              setCurrentRole(null);
              setSession(null);
              setJustLoggedIn(false);
              localStorage.removeItem('currentRole');
              localStorage.removeItem('cachedAuthUser');
            }
            setIsLoading(false);
          }, 0);
        }
      } else {
        setUser(null);
        setCurrentRole(null);
        setSession(null);
        setJustLoggedIn(false);
        // localStorage'dan currentRole ve cachedAuthUser'ƒ± temizle
        localStorage.removeItem('currentRole');
        localStorage.removeItem('cachedAuthUser');
        setIsLoading(false);
      }
    });

    return () => subscription.unsubscribe();
  }, []);

  const login = async (email: string, password: string): Promise<{ error?: string }> => {
    try {
      const { error } = await supabase.auth.signInWithPassword({
        email,
        password,
      });

      if (error) {
        return { error: error.message };
      }

      // Ba≈üarƒ±lƒ± giri≈ü i√ßin flag'i set et
      setJustLoggedIn(true);
      return {};
    } catch (error) {
      return { error: 'Giri≈ü yapƒ±lƒ±rken beklenmeyen bir hata olu≈ütu.' };
    }
  };

  const createUser = async (
    email: string, 
    password: string, 
    firstName: string, 
    lastName: string,
    roles: UserRole[] = ['kullanici']
  ): Promise<{ error?: string }> => {
    // Mevcut session'ƒ± tamamen koru (admin/temsilci/beyin_antrenoru i√ßin)
    const currentSession = await supabase.auth.getSession();
    const originalUser = user;
    const originalCurrentRole = currentRole;
    
    try {
      // Kullanƒ±cƒ± olu≈üturma flag'ini set et
      isCreatingUserRef.current = true;
      console.log('üîÑ Creating auth user for:', email);
      console.log('üíæ Current session preserved for:', originalUser?.email);
      
      // 1. Supabase Auth'da kullanƒ±cƒ± olu≈ütur
      const { data: authData, error: authError } = await supabase.auth.signUp({
        email,
        password,
        options: {
          data: {
            first_name: firstName,
            last_name: lastName,
          }
        }
      });

      if (authError) {
        console.error('‚ùå Auth signup error:', authError);
        // Session'ƒ± geri y√ºkle
        if (currentSession.data.session) {
          await supabase.auth.setSession(currentSession.data.session);
          setUser(originalUser);
          setCurrentRole(originalCurrentRole);
        }
        isCreatingUserRef.current = false; // Hata durumunda flag'i resetle
        return { error: authError.message };
      }

      if (!authData.user) {
        console.error('‚ùå No user returned from auth signup');
        // Session'ƒ± geri y√ºkle
        if (currentSession.data.session) {
          await supabase.auth.setSession(currentSession.data.session);
        }
        isCreatingUserRef.current = false; // Hata durumunda flag'i resetle
        return { error: 'Kullanƒ±cƒ± olu≈üturulamadƒ±.' };
      }

      console.log('‚úÖ Auth user created:', authData.user.id);

      // HEMEN session'ƒ± geri y√ºkle - yeni kullanƒ±cƒ±ya ge√ßmesin!
      if (currentSession.data.session) {
        await supabase.auth.setSession(currentSession.data.session);
        setUser(originalUser);
        setCurrentRole(originalCurrentRole);
        console.log('üîÑ Session restored to original user:', originalUser?.email);
      }

      // 2. Current user'ƒ±n users tablosundaki ID'sini bul (parent i√ßin)
      let currentUserId = null;
      if (user?.id) {
        currentUserId = user.id; // user.id zaten users tablosundaki ID
        console.log('‚úÖ Current user ID alƒ±ndƒ±, Parent ID:', currentUserId);
      } else {
        console.log('‚ö†Ô∏è Current user yok (baƒüƒ±msƒ±z kullanƒ±cƒ± olacak)');
      }

      // 3. Users tablosuna kullanƒ±cƒ± + roller tek seferde kaydet
      console.log('üîß Creating user with roles:', roles);
      console.log('üîß Parent user ID:', currentUserId);
      
      const insertData = {
        auth_user_id: authData.user.id,
        email: email,
        first_name: firstName,
        last_name: lastName,
        roles: roles,                       // JSONB array olarak direk g√∂nder
        supervisor_id: currentUserId       // Ki≈üi bazƒ±nda hiyerar≈üi (supervisor_id)
      };
      
      console.log('üîß Insert data:', insertData);
      
      // INSERT √∂ncesi session kontrol
      const currentSessionCheck = await supabase.auth.getSession();
      console.log('üîç INSERT √∂ncesi session kontrol:', {
        session_exists: !!currentSessionCheck.data.session,
        user_email: currentSessionCheck.data.session?.user?.email
      });
      
      const { data: newUserData, error: userError } = await supabase
        .from('users')
        .insert(insertData)
        .select('id, roles, supervisor_id')
        .single();

      if (userError) {
        console.error('‚ùå Users tablosuna kayƒ±t hatasƒ±:', userError);
        // Session'ƒ± geri y√ºkle
        if (currentSession.data.session) {
          await supabase.auth.setSession(currentSession.data.session);
          setUser(originalUser);
          setCurrentRole(originalCurrentRole);
        }
        isCreatingUserRef.current = false; // Hata durumunda flag'i resetle
        return { error: 'Kullanƒ±cƒ± profili olu≈üturulamadƒ±: ' + userError.message };
      }

      if (!newUserData) {
        // Session'ƒ± geri y√ºkle
        if (currentSession.data.session) {
          await supabase.auth.setSession(currentSession.data.session);
          setUser(originalUser);
          setCurrentRole(originalCurrentRole);
        }
        isCreatingUserRef.current = false; // Hata durumunda flag'i resetle
        return { error: 'Kullanƒ±cƒ± ID alƒ±namadƒ±.' };
      }

      console.log('‚úÖ Kullanƒ±cƒ± ba≈üarƒ±yla olu≈üturuldu!', {
        id: newUserData.id,
        roles: newUserData.roles,
        supervisor_id: newUserData.supervisor_id
      });

      // BA≈ûARI: Orijinal session'ƒ± geri y√ºkle (admin/temsilci/beyin_antrenoru)
      if (currentSession.data.session) {
        console.log('üîÑ Restoring original session for:', originalUser?.email);
        await supabase.auth.setSession(currentSession.data.session);
        
        // State'leri direkt g√ºncelle - timing sorununu √ß√∂z
        setUser(originalUser);
        setCurrentRole(originalCurrentRole);
        console.log('‚úÖ Original session restored successfully');
      }
      
      // Ba≈üarƒ±yla tamamlandƒ± - flag'i resetle
      setTimeout(() => {
        isCreatingUserRef.current = false;
      }, 2000); // 2 saniye bekle
      return {};
    } catch (error) {
      console.error('Kullanƒ±cƒ± olu≈üturma hatasƒ±:', error);
      
      // HATA: Orijinal session'ƒ± geri y√ºkle (admin/temsilci/beyin_antrenoru)
      if (currentSession.data.session) {
        console.log('üîÑ Restoring original session after error for:', originalUser?.email);
        await supabase.auth.setSession(currentSession.data.session);
        setUser(originalUser);
        setCurrentRole(originalCurrentRole);
      }
      
      // Hata durumunda flag'i resetle
      isCreatingUserRef.current = false;
      return { error: 'Kullanƒ±cƒ± olu≈üturulurken beklenmeyen bir hata olu≈ütu.' };
    }
  };

  const logout = async (): Promise<void> => {
    try {
      await supabase.auth.signOut();
      setUser(null);
      setSession(null);
      setCurrentRole(null);
      setJustLoggedIn(false); // Flag'i reset et
      // localStorage'dan currentRole ve cachedAuthUser'ƒ± temizle
      localStorage.removeItem('currentRole');
      localStorage.removeItem('cachedAuthUser');
      toast.success('Ba≈üarƒ±yla √ßƒ±kƒ±≈ü yapƒ±ldƒ±');
    } catch (error) {
      console.error('Error during logout:', error);
      toast.error('√áƒ±kƒ±≈ü yapƒ±lƒ±rken bir hata olu≈ütu');
    }
  };

  const resetPassword = async (email: string): Promise<{ error?: string }> => {
    try {
      const redirectUrl = `${window.location.origin}/`;
      
      const { error } = await supabase.auth.resetPasswordForEmail(email, {
        redirectTo: redirectUrl,
      });

      if (error) {
        return { error: error.message };
      }

      return {};
    } catch (error) {
      return { error: '≈ûifre sƒ±fƒ±rlama e-postasƒ± g√∂nderilirken bir hata olu≈ütu.' };
    }
  };

  const switchRole = (role: UserRole) => {
    if (user && user.roles.includes(role)) {
      const updatedUser = { ...user, currentRole: role };
      setCurrentRole(role);
      setUser(updatedUser);
      // Se√ßilen role'√º ve g√ºncel user'ƒ± localStorage'a kaydet
      localStorage.setItem('currentRole', role);
      try {
        localStorage.setItem('cachedAuthUser', JSON.stringify(updatedUser));
      } catch (error) {
        console.error('Error updating cached user:', error);
      }
      toast.success(`Rol deƒüi≈ütirildi: ${role}`);
    } else {
      toast.error('Bu role ge√ßi≈ü yapma yetkiniz yok');
    }
  };

  // Global window function for HTML tests to access user data
  useEffect(() => {
    (window as any).getAuthUser = () => user;
    
    return () => {
      delete (window as any).getAuthUser;
    };
  }, [user]);

  const value: AuthContextType = {
    user,
    session,
    currentRole,
    switchRole,
    login,
    createUser,
    logout,
    resetPassword,
    isLoading,
  };

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  );
}

export function useAuth() {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
}